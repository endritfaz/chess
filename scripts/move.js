class Move {
    sourceSquare;
    targetSquare;
    sourcePiece;
    targetPiece;

    constructor(sourceSquare, targetSquare, sourcePiece, targetPiece) {
        this.sourceSquare = sourceSquare;
        this.targetSquare = targetSquare;
        this.sourcePiece = sourcePiece;
        this.targetPiece = targetPiece;
    }

}